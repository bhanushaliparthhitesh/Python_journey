l1 = [1, 34 ,87 ,2 , 3, 5]
l1.sort()  # Sorting the list in ascending order
print(l1)
l1.reverse()  # Reversing the order of the list
print(l1)
l1.insert(3, 4) # Inserting an element at index 3
print(l1)  # Inserting an element at index 3
l1.remove(87)  # Removing the element 87 from the list
print(l1)
l1.pop()  # Removing the last element from the list
print(l1)
print(l1.pop(2))  # Removing and printing the element at index 2
